def _get_typescript_files(template, project_name, features):
    """Generate TypeScript template files"""
    
    package_json = {
        "name": project_name,
        "version": "1.0.0",
        "description": f"MCP Server - {project_name}",
        "main": "dist/server.js",
        "scripts": {
            "build": "tsc",
            "start": "node dist/server.js",
            "dev": "ts-node src/server.ts"
        },
        "dependencies": {},
        "devDependencies": {
            "typescript": "^5.0.0",
            "@types/node": "^20.0.0",
            "ts-node": "^10.9.0"
        }
    }
    
    server_ts = f'''import {{ MCPServer }} from './types';

class {project_name.replace('-', '').title()}Server implements MCPServer {{
    name = "{project_name}";
    version = "1.0.0";
    
    tools = [
        {{
            name: "hello_world",
            description: "Say hello to the world",
            parameters: {{
                type: "object",
                properties: {{
                    name: {{ type: "string" }}
                }}
            }}
        }},
        {{
            name: "echo",
            description: "Echo back the input message",
            parameters: {{
                type: "object",
                properties: {{
                    message: {{ type: "string" }}
                }},
                required: ["message"]
            }}
        }}
    ];
    
    async callTool(name: string, params: any): Promise<any> {{
        switch(name) {{
            case "hello_world":
                return {{ success: true, result: `Hello, ${{params.name || 'World'}}! 👋` }};
            case "echo":
                return {{ success: true, result: params.message }};
            default:
                return {{ success: false, error: `Tool not found: ${{name}}` }};
        }}
    }}
}}

const server = new {project_name.replace('-', '').title()}Server();

// Stdio MCP communication
process.stdin.on('data', (data) => {{
    try {{
        const request = JSON.parse(data.toString().trim());
        const {{ method, params, id }} = request;
        
        let result: any;
        
        if (method === 'initialize') {{
            result = {{
                protocolVersion: '2024-11-05',
                serverInfo: {{ name: server.name, version: server.version }}
            }};
        }} else if (method === 'tools/list') {{
            result = {{ tools: server.tools }};
        }} else if (method === 'tools/call') {{
            result = server.callTool(params.name, params.arguments);
        }} else {{
            result = {{ error: {{ code: -32601, message: `Method not found: ${{method}}` }} }};
        }}
        
        console.log(JSON.stringify({{ jsonrpc: '2.0', id, result }}));
    }} catch (e) {{
        console.log(JSON.stringify({{ jsonrpc: '2.0', error: {{ code: -32700, message: 'Parse error' }} }}));
    }}
}});
'''

    tsconfig = {
        "compilerOptions": {
            "target": "ES2020",
            "module": "commonjs",
            "outDir": "./dist",
            "rootDir": "./src",
            "strict": True,
            "esModuleInterop": True
        },
        "include": ["src/**/*"]
    }

    files = {
        'src/server.ts': server_ts,
        'src/types.ts': 'export interface MCPServer {\n  name: string;\n  version: string;\n  tools: any[];\n  callTool(name: string, params: any): Promise<any>;\n}\n',
        'package.json': json.dumps(package_json, indent=2),
        'tsconfig.json': json.dumps(tsconfig, indent=2),
        'README.md': f'# {project_name}\n\nMCP Server (TypeScript)\n'
    }
    
    return files


