def get_template_files(template, language, project_name, features=None):
    """Get template files for project generation"""
    features = features or []
    
    if language == 'python':
        return _get_python_files(template, project_name, features)
    elif language == 'typescript':
        return _get_typescript_files(template, project_name, features)
    elif language == 'javascript':
        return _get_javascript_files(template, project_name, features)
    else:
        return _get_python_files(template, project_name, features)


